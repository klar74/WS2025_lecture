# Einfache Berechnungen
a = 10
b = 3

print(f"a + b = {a + b}")    # Addition: 13
print(f"a - b = {a - b}")    # Subtraktion: 7
print(f"a * b = {a * b}")    # Multiplikation: 30
print(f"a / b = {a / b}")    # Division: 3.333...
print(f"a ** b = {a ** b}")  # Potenz: 1000

# BMI-Berechnung
gewicht = 70  # kg
groesse = 1.75  # m
bmi = gewicht / (groesse ** 2)
print(f"BMI: {bmi:.1f}")